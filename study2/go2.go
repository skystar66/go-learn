package main

import (
	"fmt"
)

func main() {

	fmt.Println("《《=============Go语言变量的使用=============》》")

	fmt.Println("Go 语言变量名由字母、数字、下划线组成，其中首个字符不能为数字。")
	fmt.Println("声明变量的一般形式是使用 var 关键字：")
	var a string="菜鸟编程"
	fmt.Println(a)


	fmt.Println("可以一次声明多个变量：")
	var num1,num2 int = 1,2
	fmt.Println(num1,num2)


	fmt.Println("《《=============变量声明=============》》")
	var str string="123456-string"

	fmt.Println(str)







}

